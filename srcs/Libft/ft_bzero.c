#include "../../incs/libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char *ptr;

	ptr = (unsigned char *)s;
	if (n != 0)
	{
		while (n > 0)
		{
			*ptr = 0;
			ptr++;
			n--;
		}
	}
}
